    private void refreshGrid() {

        DefaultTableModel dados = (DefaultTableModel) jTable1.getModel();
        dados.setNumRows(0);

        daoCategoria.findAll().stream().forEach((t) -> {
            dados.addRow(new String[]{"" + t.getCat_codigo(), t.getCat_descricao()});
        });
    }

