    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (formValidation()) {
                if (pro_codigo == 0) { //inserção
                    pro_produto = new Pro_produto();
                    pro_produto.setPro_descricao(txtDesc.getText().toUpperCase());
                    pro_produto.setPro_valorcompra(Double.parseDouble(txtValorCompra.getText().replaceAll(",", ".")));
                    pro_produto.setPro_valorvenda(Double.parseDouble(txtValorVenda.getText().replaceAll(",", ".")));
                    pro_produto.setMar_codigo((Pro_marcas) cboMarca.getSelectedItem());
                    pro_produto.setCat_codigo((Pro_categoria) cboCategoria.getSelectedItem());
                    pro_produto.setTpp_codigo((Pro_tipoproduto) cboTipoproduto.getSelectedItem());
                    pro_produto.setPro_datacadastro(new Date());
                    daoProdutos.insert(pro_produto);
                } else { //edição
                    pro_produto.setPro_descricao(txtDesc.getText().toUpperCase());
                    pro_produto.setPro_valorcompra(Double.parseDouble(txtValorCompra.getText().replaceAll(",", ".")));
                    pro_produto.setPro_valorvenda(Double.parseDouble(txtValorVenda.getText().replaceAll(",", ".")));
                    pro_produto.setMar_codigo((Pro_marcas) cboMarca.getSelectedItem());
                    pro_produto.setCat_codigo((Pro_categoria) cboCategoria.getSelectedItem());
                    pro_produto.setTpp_codigo((Pro_tipoproduto) cboTipoproduto.getSelectedItem());
                    daoProdutos.update(pro_produto);
                }
                JOptionPane.showMessageDialog(null, "Operação realizada com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                if (!chckContinue.getState()) {
                    this.dispose();
                } else {
                    txtDesc.setText("");
                    txtValorCompra.setText("");
                    cboCategoria.setSelectedItem(null);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, o registro não foi salvo!", "Alerta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

