    private void refreshCombos() {
        List<Pro_marcas> lsMarcas = daoMarcas.findAll();
        List<Pro_categoria> lsCategorias = daoCategoria.findAll();
        List<Pro_tipoproduto> lsTipoProduto = daoTipoproduto.findAll();
        DefaultComboBoxModel cboM = new DefaultComboBoxModel(new Vector(lsMarcas));
        DefaultComboBoxModel cboC = new DefaultComboBoxModel(new Vector(lsCategorias));
        DefaultComboBoxModel cboT = new DefaultComboBoxModel(new Vector(lsTipoProduto));
        cboMarca.setModel(cboM);
        cboCategoria.setModel(cboC);
        cboTipoproduto.setModel(cboT);
    }

    private void refreshForm() {
        if (pro_codigo > 0) {
            pro_produto = daoProdutos.findById(pro_codigo);
            txtDesc.setText(pro_produto.getPro_descricao());
            txtValorCompra.setText(Double.toString(pro_produto.getPro_valorcompra()));
            txtValorVenda.setText(Double.toString(pro_produto.getPro_valorcompra()));
            cboMarca.setSelectedItem(pro_produto.getMar_codigo());
            cboCategoria.setSelectedItem(pro_produto.getCat_codigo());
            cboTipoproduto.setSelectedItem(pro_produto.getTpp_codigo());
        } else {
            cboMarca.setSelectedItem(null);
            cboCategoria.setSelectedItem(null);
            cboTipoproduto.setSelectedItem(null);
        }
    }

    private Boolean formValidation() {
        String finalMessage = "Preencha corretamente os campos: \n";
        String message = "";
        if (txtDesc.getText().isEmpty()) {
            message += " * Descrição \n";
        }
        if (!txtValorCompra.getText().isEmpty()) {
            try {
                Double.parseDouble(txtValorCompra.getText().replaceAll(",", "."));
            } catch (Exception ex) {
                message += " * Valor de Compra Inválido \n";
            }
        }
        if (!txtValorVenda.getText().isEmpty()) {
            try {
                Double.parseDouble(txtValorVenda.getText().replaceAll(",", "."));
            } catch (Exception ex) {
                message += " * Valor de Venda Inválido \n";
            }
        }
        if (cboTipoproduto.getSelectedIndex() == -1) {
            message += " * Tipo de Produto";
        }
        if (!"".equals(message)) {
            finalMessage += message;
            JOptionPane.showMessageDialog(null, finalMessage, "Alerta", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

